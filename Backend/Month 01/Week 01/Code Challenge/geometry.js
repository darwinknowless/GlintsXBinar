// function untuk Menghitung Volume 2 kubus
function jumlahVolumeDuaKubus(a, b) {
    var volumeA, volumeB, total;

    volumeA = a * a * a;
    volumeB = b * b * b;
    
    total = volumeA = volumeB;

    return total;
}
// Result Hasilnya ada dibawah ini
console.log(jumlahVolumeDuaKubus(8, 4));
console.log(jumlahVolumeDuaKubus(16, 8));
console.log(jumlahVolumeDuaKubus(32, 16));

